package ru.job4j.ood.lsp.exam2and3;

public class Package {

    private final double size;

    private final double weight;

    public Package(double size, double weight) {
        this.size = size;
        this.weight = weight;
    }

    public double getSize() {
        return size;
    }

    public double getWeight() {
        return weight;
    }
}
